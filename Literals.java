/**
 * Literals
 */
public class Literals {

    public static void main(String[] args) {
        byte age = 34;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 5.16;
        boolean a = true;
        String str = "aman";
        System.out.println(age);
        System.out.println(d1);
        System.out.println(f1);
        System.out.println(ch);
        System.out.print(str);
        System.out.println(a);

    }
}
