public class Operator {
    public static void main(String[] args) {
        int a = 4;// ASSIGNMENT OPERATOR
        int b = 6 % a;// ARITHMETIC OPERATOR
        b += 3;
        System.out.println(5 == b);// COMPARISON OPERATOR
        System.out.println(6 > 7 || 6 < 7); // LOGICAL OPERATOR
        System.out.println(2 & 3);// BIT WISE OPERATOR
    }
}
